        ascent:  { label: 'A', badge: '#e11d48', color: '#fda4af', title: 'Ascent' },
        enhance: { label: 'E', badge: '#059669', color: '#6ee7b7', title: 'Enhancement' },
        common:  { label: 'C', badge: '#0284c7', color: '#7dd3fc', title: 'Common' },
